/* Programa que receba um número e mostre sua tabuada */
#include<stdio.h>
int main() {
    int n;
    printf("Digite um numero inteiro: ");
    scanf("%d",&n);
    printf("%d",n * 1);
    printf("%d",n * 2);
    printf("%d",n * 3);
    printf("%d",n * 4);
    printf("%d",n * 5);
    printf("%d",n * 6);
    printf("%d",n * 7);
    printf("%d",n * 8);
    printf("%d",n * 9);
    printf("%d",n * 10);
    return 0;
}
